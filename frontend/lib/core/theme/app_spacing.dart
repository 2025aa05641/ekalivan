/// Shared spacing scale for consistent gaps and padding across the app.
library;

/// 4px-based spacing scale used throughout the design system.
abstract final class AppSpacing {
  /// 4px.
  static const double xs = 4;

  /// 8px.
  static const double sm = 8;

  /// 16px — the design system's standard card padding/gap.
  static const double md = 16;

  /// 24px.
  static const double lg = 24;

  /// 32px.
  static const double xl = 32;

  /// 48px.
  static const double xxl = 48;
}
