/// Corner radii from the Ekalivan design system.
library;

/// Named corner-radius values used across buttons, cards, dialogs, etc.
abstract final class AppRadius {
  /// Buttons.
  static const double button = 16;

  /// Cards.
  static const double card = 18;

  /// Dialogs.
  static const double dialog = 22;

  /// Bottom sheets.
  static const double bottomSheet = 28;

  /// Text fields.
  static const double textField = 14;
}
